        <h1>Nuestras reglas</h1>
        
        <h2>Introducción</h2>
        <p>
            LOS SIGUIENTES PÁRRAFOS HAN SIDO EXTRAÍDOS Y MODIFICADOS DE LA "NORMAS COMUNITARIAS" DE FACEBOOK.<br>
            La idea que inspira la creación de esta página web es la ayuda mutua entre los emprenderos que quieran llevar a cabo su proyecto. Por ello esta debe de formase por una comunidad sana.
        </p>
        
        <p>
            Reconocemos lo importante que es que SelfIdea sea un lugar en el que las personas puedan comunicarse libremente y nos tomamos muy en serio nuestra responsabilidad a la hora de evitar cualquier tipo de abuso en nuestro servicio. Por eso hemos desarrollado un conjunto de Normas comunitarias que describen qué se permite y qué no en SelfIdea. Nuestras normas se aplican en todo el mundo y a todo tipo de contenido. Están diseñadas de forma que sean completas; por ejemplo, aquel contenido que podría no considerarse lenguaje que incita al odio puede eliminarse por infringir nuestras políticas relativas al bullying.
        </p>
        
        <p>
            El objetivo de nuestras Normas comunitarias es fomentar que las personas se expresen y crear un entorno seguro. Basamos nuestras políticas en aportaciones de nuestra comunidad y de expertos en campos como la tecnología y la seguridad pública.
        </p>
        
        <h2>Violencia</h2>
        <p>
            Pretendemos evitar todos los posibles daños en la vida real que puedan estar relacionados con el contenido de SelfIdea. Comprendemos que las personas suelen expresar desprecio o desacuerdo mediante amenazas o el fomento de la violencia a modo de broma o sin intenciones serias. Por eso intentamos tener en cuenta el lenguaje, el contexto y los detalles para distinguir los comentarios casuales del contenido que supone una amenaza creíble a la seguridad pública o personal. Al determinar si una amenaza es creíble, es posible que tengamos en cuenta otra información, como la vulnerabilidad y visibilidad pública de la persona que está siendo objeto de dicha amenaza. En los casos en los que creemos que existe riesgo real de daños físicos o amenazas directas a la seguridad pública, eliminamos el contenido, inhabilitamos las cuentas y colaboramos con las autoridades locales.
        </p>
        
        <h2>Spam</h2>
        <p>
            Nos esforzamos por limitar la propagación de spam comercial a fin de evitar anuncios falsos, fraude y brechas de seguridad, ya que todo esto menoscaba la capacidad de las personas de compartir y conectar. No permitimos que las personas usen información engañosa o imprecisa para aumentar el número de Me gusta, de seguidores o las veces que se comparte el contenido.
        </p>
        
        <h2>Contenido inapropiado</h2>
        <p>
            Creemos que las personas comparten y conectan con mayor libertad si no se sienten en el punto de mira debido a sus vulnerabilidades. Por tanto, sentimos una mayor preocupación en relación con el contenido que denominamos cruel e insensible, que definimos como aquel que se dirige a víctimas de daños físicos o emocionales graves.
        </p>
