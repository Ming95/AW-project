        <p>
            Primero asegúrate de estar loggeado. Haz click en Crear Idea en la página principal de Self Idea. Deberás rellenar un formulario que consta del nombre de la idea, la candidad que necesitas para llevar a cabo el proyecto(ten en cuenta que la cantidad recauda solo será abonada tras concluir la fecha de recaudación y siempre y cuando se haya alcanzado o sobre pasado la cifra final). Después elige la fecha en la que quieras concluir la recaudación, elige la categoría a la que pertenece tu idea, sube el contenido multimedia de tu proyecto, un archivo que consta de un curriculum de los miembros del equipo(esto ayuda a los patrocinadores a ganas confianza en tu proyecto) y una describición de tu proyecto(recuerda ser preve y claro). Finalmente como parte opcional puedes elegir si quieres poner en vente tu idea o no.
